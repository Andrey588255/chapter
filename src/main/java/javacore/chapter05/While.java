// Продемонстрировать применение оператора цикла while

package javacore.chapter05;

public class While {
    public static void main(String[] args) {
        int n = 10;
        while (n > 0) {
            System.out.println(" тaкт " + n);
            n--;
        }
    }
}     //  такт від 1 до 10
